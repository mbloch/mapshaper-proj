var assert = require('assert'),
    api = require('../../'),
    helpers = require('../helpers');

describe('additional interrupted projections', function() {
  var points = [
    [-120, 60],
    [-50, 60],
    [100, 60],
    [-80, -60],
    [0, -60],
    [120, -60]
  ];

  testProjection('igh_o', points, [
    [-14271613.32776727, 6532652.773031666],
    [-3703302.071387492, 6532652.773031666],
    [12511825.860086206, 6532652.773031666],
    [-10287927.327195032, -6532652.773031666],
    [928222.130420222, -6532652.773031666],
    [14735724.392977381, -6532652.773031666]
  ]);

  testProjection('imoll', points, [
    [-12415169.066926826, 6869064.045895816],
    [-7880301.658278492, 6869064.045895816],
    [6429753.619351001, 6869064.045895816],
    [-8410744.797647275, -6869064.045895816],
    [-401896.259088173, -6869064.045895816],
    [11611376.548750479, -6869064.045895816]
  ]);

  testProjection('imoll_o', points, [
    [-14271613.32776727, 6869064.045895816],
    [-5144263.660021586, 6869064.045895816],
    [9519059.483692316, 6869064.045895816],
    [-10620456.924572131, -6869064.045895816],
    [-845269.055590971, -6869064.045895816],
    [11521271.618332092, -6869064.045895816]
  ]);

  helpers.fwd_test('+proj=igh_o +R=6371000 +lon_0=-160',
    [[80, 60], [-10, -60]], [
      [-14271613.327767272, 6532652.773031666],
      [16679238.996683812, -6532652.773031666]
    ], 1e-6);

  helpers.fwd_test('+proj=imoll_o +R=6371000 +lon_0=-160',
    [[80, 60], [-10, -60]], [
      [-14271613.327767272, 6869064.045895816],
      [13464786.222038522, -6869064.045895816]
    ], 1e-6);

  function testProjection(id, input, expected) {
    var args = '+proj=' + id + ' +R=6371000';
    helpers.fwd_test(args, input, expected, 1e-6);
    it(id + ' is forward-only', function() {
      assert(!api.pj_init(args).inv);
    });
  }
});
