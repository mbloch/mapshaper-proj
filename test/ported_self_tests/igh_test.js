var assert = require('assert'),
    api = require('../../'),
    helpers = require('../helpers');

describe('igh.js', function() {
  var args = '+proj=igh +R=6371000';
  var points = [
    [-120, 60],
    [0, 60],
    [-150, -60],
    [-50, -60],
    [30, -60],
    [120, -60],
    [-40.001, 60],
    [-39.999, 60],
    [0, 40.7366111111111],
    [0, -40.7366111111111]
  ];
  var expected = [
    [-12415169.066926826, 6532652.773031666],
    [1392333.1956303332, 6532652.773031666],
    [-17143350.06189392, -6532652.773031666],
    [-6023857.397438047, -6532652.773031666],
    [2871736.7341266507, -6532652.773031666],
    [14271613.32776727, -6532652.773031666],
    [-7232528.24086314, 6532652.773031666],
    [-1198954.825491449, 6532652.773031666],
    [808217.5420260403, 4529704.484247915],
    [538811.6946840271, -4529704.484247915]
  ];

  helpers.fwd_test(args, points, expected, 1e-6);
  helpers.fwd_test(args + ' +lon_0=20', [
    [-20.001, 60],
    [-19.999, 60],
    [100, -60]
  ], [
    [-7232528.24086314, 6532652.773031666],
    [-1198954.8254914484, 6532652.773031666],
    [6110927.740304032, -6532652.773031666]
  ], 1e-6);

  it('is forward-only', function() {
    assert(!api.pj_init(args).inv);
  });
});
